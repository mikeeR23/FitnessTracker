package com.example.ramir.fitnesstracker;

public class MyClassParams {
    private String type;
    private String first;
    private String last;
    private String user;
    private String pass;
    private String email;

    MyClassParams(String type, String first, String last, String user, String pass, String email){
        this.type = type;
        this.first = first;
        this.last = last;
        this.user = user;
        this.pass = pass;
        this.email = email;
    }
}
